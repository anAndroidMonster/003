public class SettingModel {
    private double mLatMax;//纬度
    private double mLatMin;
    private double mLngMax;//经度
    private double mLngMin;

    public double getmLatMax() {
        return mLatMax;
    }

    public void setmLatMax(double mLatMax) {
        this.mLatMax = mLatMax;
    }

    public double getmLatMin() {
        return mLatMin;
    }

    public void setmLatMin(double mLatMin) {
        this.mLatMin = mLatMin;
    }

    public double getmLngMax() {
        return mLngMax;
    }

    public void setmLngMax(double mLngMax) {
        this.mLngMax = mLngMax;
    }

    public double getmLngMin() {
        return mLngMin;
    }

    public void setmLngMin(double mLngMin) {
        this.mLngMin = mLngMin;
    }
}
